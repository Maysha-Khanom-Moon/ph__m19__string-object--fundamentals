const computer = {
    brand: 'lenovo',
    price: 35000,
    processor: 'intel',
    hdd: '512gb'
}

/**
 * --- Object.keys(objName) ---
 * 
 * ==> make a array of object keys
 */
const KEYs = Object.keys(computer);
console.log(KEYs);
console.log('\n');


/**
 * --- Object.values(objName) ---
 * 
 * ==> make a array of object values
 */
const VALUEs = Object.values(computer);
console.log(VALUEs);